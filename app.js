function esMayorDeEdad(edad) {
  if (edad >= 18) { // si la edad es igual o mayor a 18
    console.log(edad + " es mayor de edad") // imprima Es mayor de edad
  } else { // de lo contrario
    console.log(edad + " es menor de edad") // imprima Es menor de edad
  }
}

esMayorDeEdad(17)
esMayorDeEdad(58)
esMayorDeEdad(18)
esMayorDeEdad(72)
